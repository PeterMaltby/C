#include <stdio.h>

int main() {

  printf("\n\\a = '\a'\n");
  printf("\n\\b = '\b'\n");
  printf("\n\\e = '\e'\n");
  printf("\n\\f = '\f'\n");
  printf("\n\\n = '\n'\n");
  printf("\n\\o = cant do o as it is a escape sequence.\n");
  printf("\n\\r = '\r'\n");
  printf("\n\\t = '\t'\n");
  printf("\n\\u1a1a = '\u1a1a'\n");
  printf("\n\\v = '\v'\n");
  printf("\n\\r1a = '\x1a'\n");
  return 0;
}
